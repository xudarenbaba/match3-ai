LAYOUT_NAMES = list(LAYOUTS.keys())

# curriculum 对应的布局池
LAYOUT_POOL = {
    1: ["full"],
    2: ["full", "cross", "l_shape", "t_shape"],
    3: LAYOUT_NAMES,
}


def get_layout(name: str) -> list[list[int]]:
    return LAYOUTS.get(name, LAYOUTS["full"])


def pick_layout(rng: random.Random, pool: list[str] | None = None) -> str:
    if pool is None:
        pool = LAYOUT_NAMES
    return rng.choice(pool)
