"""
布局定义：每个布局是一个 10×10 的 0/1 列表
1 = 活跃格（可填充、可参与游戏）
0 = void 格（永久空洞，不填充不参与）
"""

from __future__ import annotations
import random


def _full():
    return [[1] * 10 for _ in range(10)]


def _from_bits(rows):
    return [[int(ch) for ch in row] for row in rows]


