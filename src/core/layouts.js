/**
 * 布局定义：每个布局是一个 10×10 的 0/1 数组
 * 1 = 活跃格（可填充、可参与游戏）
 * 0 = void 格（永久空洞，不填充不参与）
 */

function full() {
  return Array.from({ length: 10 }, () => Array(10).fill(1));
}

function fromBits(rows) {
  return rows.map((row) => row.split('').map(Number));
}

