export const LAYOUT_NAMES = Object.keys(LAYOUTS);

// curriculum 1 只用 full；curriculum 2 加简单不规则；curriculum 3 全部
export const LAYOUT_POOL = {
  1: ['full'],
  2: ['full', 'cross', 'l_shape', 't_shape'],
  3: LAYOUT_NAMES,
};

/**
 * 根据布局名称获取布局掩码
 * @param {string} name
 * @returns {number[][]} 10×10 的 0/1 数组
 */
export function getLayout(name) {
  return LAYOUTS[name] || LAYOUTS.full;
}

/**
 * 随机选择一个布局名称
 * @param {string[]} pool 可选布局名称列表
 * @returns {string}
 */
export function pickLayout(pool = LAYOUT_NAMES) {
  return pool[Math.floor(Math.random() * pool.length)];
}
